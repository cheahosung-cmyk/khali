EMPLOYEES: list[Employee] = [Employee(**raw) for raw in _RAW]

_BY_ID: dict[str, Employee] = {e.id: e for e in EMPLOYEES}
_DEPT_BY_ID: dict[str, Department] = {d.id: d for d in DEPARTMENTS}


def get_employee(employee_id: str) -> Employee | None:
    """id로 직원을 찾는다. 없으면 None."""
    return _BY_ID.get(employee_id)


def list_employees(department: str | None = None, q: str | None = None) -> list[Employee]:
    """직원 목록을 반환한다. department 슬러그와 키워드 q로 필터링할 수 있다."""
    result = EMPLOYEES
    if department:
        result = [e for e in result if e.department == department]
    if q:
        needle = q.lower()
        result = [
            e
            for e in result
            if needle in e.name.lower()
            or needle in e.description.lower()
            or needle in e.id.lower()
        ]
    return result


def get_department(department_id: str) -> Department | None:
    """id로 부서를 찾는다. 없으면 None."""
    return _DEPT_BY_ID.get(department_id)
